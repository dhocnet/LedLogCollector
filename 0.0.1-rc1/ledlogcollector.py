                llc_a.close()
                llc_h = llc_ffx.split("-")
                llc_a = codecs.open("xl\\sharedStrings.xml","w",encoding="utf-16")
                llc_a.write("%s%s %s%s"%(base64.decodestring(llc_xml1),llc_bulan[llc_h[0]],llc_pil[-4:],base64.decodestring(llc_xml2)))
